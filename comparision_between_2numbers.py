a = int(input("Enter first number : "))
b = int(input("Enter second number : "))
if(a == b):
    print('First number', a, ' is equal to Second number', b)
else:
    print('The given two numbers', a, b, ' are not equal')
if(a > b):
    print('First number', a, 'is greater than Second number', b)
elif(a < b):
    print('First number', a, ' is smaller than Second number', b)
